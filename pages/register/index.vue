<script>
export default {
  name: "register-index",
  data() {
    return {
      defaultStep: 1,
      stepList: [
        { id: 1, label: "輸入手機號碼" },
        { id: 2, label: "輸入驗證碼" },
      ],

      pwdType: "password",
      registerInfo: {
        password: "",
        name: "",
        phone: "",
        isPhoneVerify: true,
        email: "",
        isEmailVerify: false,
        county: "",
        area: "",
        address: "",
        birthday: "",
        fbUserId: "",
        googleUserId: "",
        lineUserId: "",
        verifyCode: "",
      },

      // disabled btn
      disNextStep: false,
      disGoVerify: false,
    };
  },
  methods: {
    showPassword() {
      let x = document.getElementById("pwdId");
      if (x.type === "password") {
        x.type = "text";
      } else {
        x.type = "password";
      }
      this.pwdType = x.type;
    },
    async nextStep() {
      this.disNextStep = true;
      const status = await this.$refs.form.validate();

      if (status) {
        this.api.members
          .getRegistVerifyCode({ account: this.registerInfo.phone })
          .then((res) => {
            const { code } = res.data;
            if (code === 200) {
              this.defaultStep = 2;
            } else {
              this.disNextStep = false;
            }
          })
          .catch(() => {
            this.disNextStep = false;
          });
      } else {
        this.disNextStep = false;
        this.registerInfo.phone = "";
        this.$refs.phoneField.focus();
      }
    },
    async goVerify() {
      this.disGoVerify = true;
      const status = await this.$refs.vertify.validate();

      if (status) {
        this.api.members
          .add(this.registerInfo)
          .then((res) => {
            const { code } = res.data;
            if (code === 200) {
              this.$router.push({ name: "register-finish" });
            } else {
              this.disGoVerify = false;
            }
          })
          .catch(() => {
            this.disGoVerify = false;
          });
      } else {
        this.disGoVerify = false;
      }
    },
  },
};
</script>